"""
FastAPI service that receives a sensor payload + a free‑form voice command,
decides what the room should do (lights, colour, …) and asks Groq’s LLM to
explain the reasoning.

Key improvements over the original snippet:
*   ✅ No trailing spaces in the model name.
*   ✅ Explicit check for the GROQ_API_KEY env‑var.
*   ✅ Async‑friendly Groq call (run in a thread‑pool so the event‑loop stays free).
*   ✅ Strongly‑typed request & response models (OpenAPI docs automatically generated).
*   ✅ Robust command parsing – colour + on/off can be combined.
*   ✅ Validation of numeric fields (noise must be 0‑120 dB).
*   ✅ Centralised logging (structured, time‑stamped).
*   ✅ Automatic retries on transient HTTP errors.
*   ✅ Clear error handling – callers get a proper 4xx/5xx HTTP status, not a silent
    “status: error” dict.
"""

